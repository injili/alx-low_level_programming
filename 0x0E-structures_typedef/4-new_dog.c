#include <stdlib.h>
#include "dog.h"

/**
 * new_dog - creates a new dog
 * @name:  the new dog name
 * @age: the new dog age
 * @owner: who owns the new dog?
 * 
 * Return: Null if function fails
 */

dog_t *new_dog(char *name, float age, char *owner)
{
	dog_t.name = name;
	dog_t.age = age;
	dog_t.owner = owner;
}
